package DEMo_4;

public class Loops {

	public static void main(String[] args) {

       int y=0;
       int k=0;
		//for loop 
		
		for(int i=0;i<=4;i++)
		{
			System.out.println("Nidhi is testing -->" + i);
		}
		
		//while
		
		while (y<=4)
		{
			System.out.println("testdata-->" +y);
			y++;
		}
		
		
		//do while 
		
		
		do {
			System.out.println("test nidhi ==>" + k);
			k++;
		}
		while(k<=4);
	}

}
